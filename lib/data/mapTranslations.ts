// 兜底音译表：当单词不在 fallbackWordMap 中时，按字母做简单音译，保证最终一定显示中文
const pinyinMap: Record<string, string> = {
  a: '阿', b: '布', c: '克', d: '德', e: '伊', f: '夫', g: '格',
  h: '赫', i: '伊', j: '杰', k: '克', l: '尔', m: '姆', n: '恩',
  o: '奥', p: '普', q: '库', r: '尔', s: '斯', t: '特', u: '乌',
  v: '夫', w: '沃', x: '克', y: '伊', z: '兹',
};

function transliterate(word: string): string {
  return word
    .toLowerCase()
    .split('')
    .map((ch) => pinyinMap[ch] || ch)
    .join('');
}

function translateMapName(name: string): string {
  // 分词并翻译
  const words = name.split(/[\s\-]+/).filter(Boolean);
  const translated = words
    .map((w) => {
      const key = w.toLowerCase().replace(/[^a-z0-9']/g, '');
      if (!key) return w;
      const mapped = fallbackWordMap[key];
      // mapped 可能为空字符串（如 the/a/an 等冠词删除），这是合法的
      if (mapped !== undefined) return mapped;
      // 未命中时做简单音译，确保不保留英文
      return transliterate(key);
    })
    .filter((part) => part !== '');

  // 清理首尾多余的 "之"
  let result = translated.join('');
  result = result.replace(/^之+|之+$/g, '');
  return result || name;
}

