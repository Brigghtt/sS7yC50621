function translateMapName(name: string): string {
  // 分词并翻译
  const words = name.split(/[\s\-]+/).filter(Boolean);
  const translated = words
    .map((w) => {
      const key = w.toLowerCase().replace(/[^a-z0-9']/g, '');
      if (!key) return w;
      const mapped = fallbackWordMap[key];
      // mapped 可能为空字符串（如 the/a/an 等冠词删除），这是合法的
      if (mapped !== undefined) return mapped;
      // 未命中时保留英文原词，避免字母音译产生无意义中文
      return w;
    })
    .filter((part) => part !== '');

  // 清理首尾多余的 "之"
  let result = translated.join('');
  result = result.replace(/^之+|之+$/g, '');
  return result || name;
}

// 大小写不敏感查找表：API 返回的地图名大小写可能与本地维护表不一致
const mapNameCnMapInsensitive: Record<string, string> = Object.fromEntries(
  Object.entries(mapNameCnMap).map(([k, v]) => [k.toLowerCase(), v])
);

/**
 * 获取地图中文名；未命中精确映射时尝试机器翻译兜底。
 */
export function getMapNameCn(name: string | undefined | null): string {
  if (!name) return "未知地图";
  return (
    mapNameCnMap[name] ??
    mapNameCnMapInsensitive[name.toLowerCase()] ??
    translateMapName(name)
  );
}
